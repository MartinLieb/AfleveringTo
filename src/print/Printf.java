package print;

public class Printf {

    public static void main(String[] args) {

        // printf er en anden måde at printe ting i konsolen hvor vi kan få formater med
        // her printer vi en sætning med 2 formater

        int myInt = 10; // her defineres en variabel
        String myString = "damer der er vild med mig"; // her defineres en variabel

        System.out.printf("Jeg har %d %s", myInt, myString); // her printer vi en kæmpe løgn

    }
}
